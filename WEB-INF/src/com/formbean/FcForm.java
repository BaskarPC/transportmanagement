package com.formbean;


public class FcForm extends GeneralForm {
	
	private String fcReason;
	private String totalAmount;
	/**
	 * @return the fcReason
	 */
	public String getFcReason() {
		return fcReason;
	}
	/**
	 * @param fcReason the fcReason to set
	 */
	public void setFcReason(String fcReason) {
		this.fcReason = fcReason;
	}
	/**
	 * @return the totalAmount
	 */
	public String getTotalAmount() {
		return totalAmount;
	}
	/**
	 * @param totalAmount the totalAmount to set
	 */
	public void setTotalAmount(String totalAmount) {
		this.totalAmount = totalAmount;
	}
	
}
